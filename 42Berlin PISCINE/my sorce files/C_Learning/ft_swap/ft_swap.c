#include <stdio.h>

void     ft(int *a, int *b)
{
     int tmp;
     tmp=*a;
     *a=*b;
     *b=tmp;
     
}


int main()
{
    int a=2;
    int b=3;
    
    printf("%d\n",a);
    printf("%d\n",b);
    ft(&a,&b);
    printf("%d\n",a);
    printf("%d\n",b);
}
