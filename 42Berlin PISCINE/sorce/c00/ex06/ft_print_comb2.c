#include <unistd.h>

void	ft_print_comb2(void)
{
	char	array[5];
	int		a;
	int		b;

	a = 0;
	b = 0;
	array[2] = ' ';
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			array[0] = (a / 10) + '0';
			array[1] = (a % 10) + '0';
			array[3] = (b / 10) + '0';
			array[4] = (b % 10) + '0';
			write (1, &array, 5);
			if (a != 98 || b != 99)
				write(1, ", ", 2);
			b++;
		}
		a++;
	}
}
