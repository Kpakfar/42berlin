int	ft_sqrt(int nb)
{
	long	i;
	long	num;

	num = nb;
	if (num <= 0)
		return (0);
	if (num == 1)
		return (1);
	i = 2;
	if (num >= 2)
	{
		while (i * i <= num)
		{
			if (i * i == num)
			{
				return (i);
			}
			i++;
		}
	}
	return (0);
}
